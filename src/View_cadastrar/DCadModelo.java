    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTNome.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o nome do modelo!", "ATENÇÂO", JOptionPane.WARNING_MESSAGE);
            jTNome.requestFocus();
        } else {
            Connection conexao = Conexao.conectar();
            ModeloDAO modeloDAO = new ModeloDAO(conexao);
            if (!modeloDAO.existeNomeModelo(jTNome.getText())) {
                Modelo modelo = new Modelo(
                        0,
                        jTNome.getText(),
                        jTDescricao.getText(),
                        jCPorte.getSelectedItem().toString().toLowerCase(),
                        new MarcaDAO(conexao).retornaIdMarca(jCIdMarca.getSelectedItem().toString())
                );
                if (modeloDAO.addModelo(modelo)) {
                    limparModelos();
                    JOptionPane.showMessageDialog(rootPane, "Modelo Cadastrado!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro inesperado!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Modelo já existente!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

