    public void preencherMarca() {

        Connection conexao = Conexao.conectar();
        MarcaDAO marcaDAO = new MarcaDAO(conexao);
        List<Marca> listaMarca = new ArrayList<>();
        listaMarca = marcaDAO.listarMarcas();
        for (Marca m : listaMarca) {
            jCIdMarca.addItem(m.getNome());
        }
        Conexao.desconectar(conexao);

    }

    public void limparModelos() {
        jTNome.setText("");
        jTDescricao.setText("");
    }

