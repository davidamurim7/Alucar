    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTnome.getText().trim().equals("")
                || jTemail.getText().trim().equals("")
                || jTendereco.getText().trim().equals("")
                || jData.getDate() == null
                || jFcpf.getText().length() != 14
                || jFtelefone.getText().length() != 14) {
            JOptionPane.showMessageDialog(rootPane, "Preencha corretamente todos os campos de texto", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else if (!Metodos.validarEmail(jTemail.getText())) {
            JOptionPane.showMessageDialog(rootPane, "email inv√°lido", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            ClienteDAO clienteDAO = new ClienteDAO(conexao);

            if (!clienteDAO.existeCliente(jFcpf.getText().trim())) {

                Cliente cliente = new Cliente();
                cliente.setNome(jTnome.getText());
                cliente.setCpf(jFcpf.getText());
                cliente.setTelefone(jFtelefone.getText());
                cliente.setDataNascimento(jData.getDate());
                cliente.setEmail(jTemail.getText());
                cliente.setEndereco(jTendereco.getText());
                //atualizando logotipo

                if (clienteDAO.addCliente(cliente)) {
                    limpar();
                    telaPrincipal.preencherClientes();
                    JOptionPane.showMessageDialog(rootPane, "Cliente Cadastrado!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro inesperado!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Cliente j√° existente!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

