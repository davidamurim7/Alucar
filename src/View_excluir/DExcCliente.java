    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (bkpCpf.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "CPF não selecionado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            ClienteDAO clienteDAO = new ClienteDAO(conexao);
            Cliente cliente = new Cliente();
            cliente.setCpf(bkpCpf);

            if (clienteDAO.delCliente(cliente)) {
                limpar();
                telaPrincipal.preencherClientes();
                JOptionPane.showMessageDialog(rootPane, "Cliente excluído com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro inesperado!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        if (jLCpf.getText().length() != 14) {
            limpar();
            JOptionPane.showMessageDialog(rootPane, "Campo \"CPF\" inválido!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            ClienteDAO clienteDAO = new ClienteDAO(conexao);
            if (clienteDAO.existeCliente(jLCpf.getText().trim())) {
                preencheCampos(jLCpf.getText().trim());
            } else {
                limpar();
                JOptionPane.showMessageDialog(rootPane, "Cliente não cadastrado!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }

    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jLCpfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jLCpfKeyReleased
        if ((!bkpCpf.equals(""))) {
            if (!jLCpf.getText().trim().equals(bkpCpf)) {
                jLCpf.setForeground(Color.red);
            } else {
                jLCpf.setForeground(Color.black);
            }
        }
    }//GEN-LAST:event_jLCpfKeyReleased

