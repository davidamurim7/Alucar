    public void preencheCampos(String cpf) {
        limpar();
        bkpCpf = cpf;
        Connection conexao = Conexao.conectar();
        ClienteDAO clienteDAO = new ClienteDAO(conexao);
        Cliente cliente = clienteDAO.selecionaCliente(cpf);
        jLCpf.setText(cliente.getCpf());
        jLNascimento.setText("Data de Nascimento: " + new SimpleDateFormat("dd/MM/yyyy").format(cliente.getDataNascimento()));
        jLCliente.setText("Nome: " + cliente.getNome());
        jLTelefone.setText("Telefone: " + cliente.getTelefone());
        jLEmail.setText("Email: " + cliente.getEmail());
        jLEndereco.setText("Endereço: " + cliente.getEndereco());
        Conexao.desconectar(conexao);
    }

