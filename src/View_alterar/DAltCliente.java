    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jFcpf.getText().length() != 14
                || jDNascimento.getDate() == null
                || jTCliente.getText().trim().equals("")
                || jFTelefone.getText().length() != 14
                || jTEmail.getText().trim().equals("")
                || jTEndereco.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Preencha corretamente todos os campos de texto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else if (!Metodos.validarEmail(jTEmail.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Email incorreto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            ClienteDAO clienteDAO = new ClienteDAO(conexao);// TODO add your handling code here:

            Cliente cliente = new Cliente();
            cliente.setCpf(bkpCpf);
            cliente.setDataNascimento(jDNascimento.getDate());
            cliente.setNome(jTCliente.getText());
            cliente.setTelefone(jFTelefone.getText());
            cliente.setEmail(jTEmail.getText());
            cliente.setEndereco(jTEndereco.getText());

            if (clienteDAO.altCliente(cliente)) {
                limpar();
                telaPrincipal.preencherClientes();
                JOptionPane.showMessageDialog(rootPane, "Cliente Atualizado!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro inesperado!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }

            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jBbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBbuscarActionPerformed
        if (jFcpf.getText().length() != 14) {
            limpar();
            JOptionPane.showMessageDialog(rootPane, "Campo \"CPF\" vazio!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conexao = Conexao.conectar();
            ClienteDAO clienteDAO = new ClienteDAO(conexao);
            if (clienteDAO.existeCliente(jFcpf.getText().trim())) {
                preencheCampos(jFcpf.getText().trim());
            } else {
                limpar();
                JOptionPane.showMessageDialog(rootPane, "CPF não cadastrado!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }

    }//GEN-LAST:event_jBbuscarActionPerformed

    private void jFcpfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFcpfKeyReleased
        if ((!bkpCpf.equals(""))) {
            if (!jFcpf.getText().trim().equals(bkpCpf)) {
                jFcpf.setForeground(Color.red);
            } else {
                jFcpf.setForeground(Color.black);
            }
        }
    }//GEN-LAST:event_jFcpfKeyReleased

