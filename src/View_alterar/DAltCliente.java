    public void preencheCampos(String cpf) {
        limpar();
        bkpCpf = cpf;
        Connection conexao = Conexao.conectar();
        ClienteDAO clienteDAO = new ClienteDAO(conexao);
        Cliente cliente = clienteDAO.selecionaCliente(cpf);
        jFcpf.setText(cliente.getCpf());
        jDNascimento.setDate(cliente.getDataNascimento());
        jTCliente.setText(cliente.getNome());
        jFTelefone.setText(cliente.getTelefone());
        jTEmail.setText(cliente.getEmail());
        jTEndereco.setText(cliente.getEndereco());
        Conexao.desconectar(conexao);
    }

