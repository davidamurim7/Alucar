    public void preencherTabela() {

        Connection conexao = Conexao.conectar();
        ModeloDAO modeloDAO = new ModeloDAO(conexao);
        MarcaDAO marcaDAO = new MarcaDAO(conexao);
        List<Modelo> listaModelo = new ArrayList<>();
        DefaultTableModel model = (DefaultTableModel) jTModelos.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        int i = 0;
        listaModelo = modeloDAO.listarModelo();
        for (Modelo m : listaModelo) {
            model.addRow(new String[i]);
            jTModelos.setValueAt(m.getNome(), i, 0);
            jTModelos.setValueAt(m.getPorte(), i, 1);
            jTModelos.setValueAt(marcaDAO.selecionaMarca(m.getIdMarca()).getNome(), i, 2);
            jTModelos.setValueAt(m.getDescricao(), i, 3);
            i++;
        }
        Conexao.desconectar(conexao);

    }

